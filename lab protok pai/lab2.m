% --- Executes on button press in pushbutton2.
function pushbutton2_Callback(hObject, eventdata, handles)
global AI       %globalne varijable
global stop
 
kanal = str2num(get(handles.edit1, 'String'));
brsec = str2num(get(handles.edit2, 'String'));
samp = str2num(get(handles.edit3, 'String'));
refresh = str2num(get(handles.edit4, 'String'));
 
AI = analoginput('winsound');
addchannel(AI,kanal);            
set(AI,'SampleRate',samp);   
set(AI, 'SamplesPerTrigger',brsec*samp);
 
start(AI);
 
br=1;
stop=0;
while AI.SamplesAcquired <= AI.SamplesPerTrigger      
    if stop == 1;
        delete AI
        return
    end
     while AI.SamplesAcquired < refresh*samp*br && refresh*br<=brsec
    end
    
        data=peekdata(AI,refresh*samp)            
        t=linspace(refresh*(br-1),refresh*br,refresh*samp)
        %[data,t]=getdata(AI) 
        plot(handles.axes1,t,data,'r');
        [c,d]=size(data);
        max=data(1);
        for i=2:d
            if (data(i)>max)
                max=data(i)
            end
        end
        %set (
            
       drawnow
       
       br=br+1
end 
    grid on
xlabel(handles.axes1,'x-osa');
ylabel(handles.axes1,'y-osa');
title(handles.axes1,'Akvizicija zvucnog signala');
delete (AI)                          
clear all

