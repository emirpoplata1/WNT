grid on;
xlabel(handles.axes2,'x-osa');
ylabel(handles.axes2,'y-osa');
title(handles.axes2,'Prikaz izlaznog signala');
AO = analogoutput('winsound'); 
addchannel(AO,1)                    
set(AO,'SampleRate',smpl*brsec);        
putdata(AO,5);                     
start(AO);                          
delete (AO)                         
                                    
clear all

